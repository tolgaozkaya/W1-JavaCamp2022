public class DortIslem {

    public int Topla(int sayil, int sayi2) {
        return sayil + sayi2;
    }

    public int Cikar(int sayil, int sayi2) {
        return sayil - sayi2;

    }

    public int Carp(int sayil, int sayi2) {
        return sayil * sayi2;
    }

    public int Bol(int sayil, int sayi2) {
        return sayil / sayi2;
    }
}
